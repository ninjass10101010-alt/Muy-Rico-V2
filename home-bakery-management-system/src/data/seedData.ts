export function generateOrderNumber(existing: Order[]): string {
  const nums = existing
    .map((o) => parseInt(o.orderNumber.replace(/\D/g, ""), 10))
    .filter((n) => !Number.isNaN(n));
  const max = nums.length ? Math.max(...nums) : 1000;
  return `MR-${max + 1}`;
}
